    protected void readyForNextImage() {
        if (postInferenceCallback != null) {
            postInferenceCallback.run();
        }
    }

    protected int getScreenOrientation() {
        switch (getWindowManager().getDefaultDisplay().getRotation()) {
            case Surface.ROTATION_270:
                return 270;
            case Surface.ROTATION_180:
                return 180;
            case Surface.ROTATION_90:
                return 90;
            default:
                return 0;
        }
    }

    private long baseTimer;

    private void startTimer() {
        mTimerTask = new TimerTask() {
            @Override
            public void run() {
                if (0 == CameraActivity.this.baseTimer) {
                    CameraActivity.this.baseTimer = SystemClock.elapsedRealtime();
                }
                totaltimeCount = (int) ((SystemClock.elapsedRealtime() - CameraActivity.this.baseTimer) / 1000);
                String hh = new DecimalFormat("00").format(totaltimeCount / 3600);
                String mm = new DecimalFormat("00").format(totaltimeCount % 3600 / 60);
                String ss = new DecimalFormat("00").format(totaltimeCount % 60);
                timeCount = new String(hh + ":" + mm + ":" + ss);
                runOnUiThread(new Runnable() {
                    @Override
                    public void run() {
                        tv_record.setText(timeCount);
                    }
                });
            }
        };
        mTimer = new Timer();
        mTimer.schedule(mTimerTask, 0, 1000);
    }


